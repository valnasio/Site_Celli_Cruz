create or replace function public.sync_identity_sequences()
returns void
language plpgsql
security definer
set search_path = public
as $$
begin
  perform setval(pg_get_serial_sequence('public.about_mission_carousel', 'id'), coalesce((select max(id) from public.about_mission_carousel), 1), true);
  perform setval(pg_get_serial_sequence('public.carousel_items', 'id'), coalesce((select max(id) from public.carousel_items), 1), true);
  perform setval(pg_get_serial_sequence('public.whatsapp_options', 'id'), coalesce((select max(id) from public.whatsapp_options), 1), true);
  perform setval(pg_get_serial_sequence('public.imoveis', 'id'), coalesce((select max(id) from public.imoveis), 1), true);
  perform setval(pg_get_serial_sequence('public.imovel_plantas', 'id'), coalesce((select max(id) from public.imovel_plantas), 1), true);
  perform setval(pg_get_serial_sequence('public.imovel_comodos', 'id'), coalesce((select max(id) from public.imovel_comodos), 1), true);
end;
$$;

revoke all on function public.sync_identity_sequences() from public;
grant execute on function public.sync_identity_sequences() to service_role;
