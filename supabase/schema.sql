create extension if not exists pgcrypto;

create or replace function public.touch_updated_at()
returns trigger
language plpgsql
as $$
begin
  new.updated_at = now();
  return new;
end;
$$;

create table if not exists public.site_config (
  id smallint primary key default 1,
  nome_empresa text,
  telefone text,
  whatsapp text,
  whatsapp_vendas text,
  whatsapp_atendimento text,
  email text,
  endereco text,
  hero_chamada text,
  hero_subtitulo text,
  google_maps_embed text,
  updated_at timestamptz not null default now(),
  constraint site_config_singleton check (id = 1)
);

create table if not exists public.site_about (
  id smallint primary key default 1,
  about_title text,
  about_subtitle text,
  about_description text,
  about_summary text,
  about_mission_title text,
  about_mission_text text,
  mission_gallery text[] not null default '{}',
  updated_at timestamptz not null default now(),
  constraint site_about_singleton check (id = 1)
);

create table if not exists public.about_mission_carousel (
  id bigint generated by default as identity primary key,
  title text,
  subtitle text,
  image_desktop text,
  image text,
  image_source text,
  image_mobile text,
  image_mobile_source text,
  sort_order integer not null default 0,
  created_at timestamptz not null default now()
);

create table if not exists public.carousel_items (
  id bigint generated by default as identity primary key,
  title text,
  subtitle text,
  image_desktop text,
  image text,
  image_source text,
  image_mobile text,
  image_mobile_source text,
  link text,
  sort_order integer not null default 0,
  created_at timestamptz not null default now()
);

create table if not exists public.whatsapp_options (
  id bigint generated by default as identity primary key,
  title text not null,
  description text,
  whatsapp text not null,
  created_at timestamptz not null default now()
);

create table if not exists public.imoveis (
  id bigint generated by default as identity primary key,
  destaque boolean not null default false,
  nome text not null,
  bairro text,
  cidade text,
  quartos smallint,
  metragem text,
  descricao text,
  tag text,
  imagem text,
  imagem_source text,
  imagem_galeria text[] not null default '{}',
  diferenciais text[] not null default '{}',
  localizacao text,
  maps_link text,
  status text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.imovel_plantas (
  id bigint generated by default as identity primary key,
  imovel_id bigint not null references public.imoveis(id) on delete cascade,
  nome text,
  descricao text,
  unidades smallint,
  sort_order integer not null default 0
);

create table if not exists public.imovel_comodos (
  id bigint generated by default as identity primary key,
  imovel_id bigint not null references public.imoveis(id) on delete cascade,
  nome text,
  fotos text[] not null default '{}'
);

create index if not exists idx_about_mission_carousel_sort on public.about_mission_carousel(sort_order, id);
create index if not exists idx_carousel_items_sort on public.carousel_items(sort_order, id);
create index if not exists idx_imoveis_destaque on public.imoveis(destaque) where destaque = true;
create index if not exists idx_imoveis_status on public.imoveis(status);
create index if not exists idx_imoveis_bairro on public.imoveis(bairro);
create index if not exists idx_imovel_plantas_imovel on public.imovel_plantas(imovel_id, sort_order, id);
create index if not exists idx_imovel_comodos_imovel on public.imovel_comodos(imovel_id, id);

drop trigger if exists trg_site_config_touch on public.site_config;
